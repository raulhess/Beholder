<div class="section">
  <h1>Era da Escuridão, ano desconhecido</h1>
  <p>
    Antes da chegada dos Nerevar ao mundo, os mortais viveram na chamada era da escuridão. Isso porque
    eles não tinham conhecimento nenhum, apenas seguiam seus instintos de sobrevivência. Durante esse período
    não foram criadas comunidades específicas. Os únicos registros que existem são de pinturas feitas em cavernas
    que os mortais utilizavam para se abrigar. Para ser honesto, é possível que alguns elfos tenham começado a
    se desenvolver já nessa época, se adaptando às selvas. Entretanto, os idiomas espalhados no mundo, a arte
    da magia, o hábito de marcar a passagem dos anos, etc., só chegaram quando os Nerevar caíram dos céus.
  </p>
</div>
<div class="section">
  <h1>Era dos Nerevar, 0 até 500</h1>
  <p>
    Um dia, enquanto os mortais viviam na era das trevas, mal conseguindo sobreviver, sem ter condições de enfrentar
    os climas desolados, os montros que habitavam o mundo, a escassez de recurso, os Nerevar chegaram. Eles
    vieram através de cometas, rochas enormes feitos de um material conhecido como Pedra Celeste, caindo em
    diversas partes do mundo, do mundo que eles criaram. Como dizem as lendas, os Nerevar vivam em um plano
    distante do mundo dos mortais, e utilizando seus poderes forjaram a nossa terra. É dito que primeiramente
    todos os continentes eram unidos, porém com as frequentes disputas entre eles mesmos, houve uma divisão
    de territórios, separados entre os grupos deles.
  </p>
  <p>
    Os Nerevar apresentaram um conceito que os mortais ainda não conheciam, o conceito da divindade.
  </p>
</div>
